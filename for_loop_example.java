public class for_loop_example {
    public static void main(String[] args){
       // this is an example of for loop
        // for printing of even number
        for(int i=10;i>=0;i-=2){
            System.out.println(i);
        }
        // this is another method
        for(int i=10;i>=0;i--){
            if(i%2==0)
            System.out.println(i);
        }
    }
}
